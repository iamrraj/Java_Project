    public void getEmpTable(){
        DefaultTableModel emp = new DefaultTableModel(new String[]{"ID","Name", "Department", "Salary"}, 0);
        try{
            st = conn.createStatement();
            String sql="SELECT * FROM teacher";
            rs = st.executeQuery(sql);
            while(rs.next())
            {
                String col1 = rs.getString("ID");
                String col2 = rs.getString("Last_Name");
                String col3 = rs.getString("Departmenty");
                String col4 = rs.getString("salary");                
                
                emp.addRow(new Object[]{col1, col2, col3, col4});
            }
            jTableEmp.setModel(emp);
            rs.close();
            st.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    private void searchEmp(){
        DefaultTableModel emp = new DefaultTableModel(new String[]{"ID","Name", "Department", "Salary"}, 0);
        try{
            st = conn.createStatement();
            String sql= null;
            if(jComboBoxSearch.getSelectedItem().equals("Search By : Name")){
                sql = "SELECT * FROM teacher WHERE Last_Name like '"+jTextFieldSearch.getText()+"%'"; 
            }
            else if(jComboBoxSearch.getSelectedItem().equals("ID")){
                sql = "SELECT * FROM teacher WHERE ID like '"+jTextFieldSearch.getText()+"%'";
            }
            else if(jComboBoxSearch.getSelectedItem().equals("Department")){
                sql = "SELECT * FROM teacher WHERE Department like '"+jTextFieldSearch.getText()+"%'";
            }
            else{
                sql = "SELECT * FROM teacher WHERE salary like '"+jTextFieldSearch.getText()+"%'";
            }
            rs = st.executeQuery(sql);
            while(rs.next())
            {
                String col1 = rs.getString("ID");
                String col2 = rs.getString("Last_Name");
                String col3 = rs.getString("Departmenty");
                String col4 = rs.getString("salary");
                emp.addRow(new Object[]{col1, col2, col3, col4});
            }
            jTableEmp.setModel(emp);
            rs.close();
            st.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    

