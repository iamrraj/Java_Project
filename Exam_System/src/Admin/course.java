    public void add()
    {
         if(!id.getText().equals("") && !course.getText().equals("") && !hour.getText().equals("") && !sec.getText().equals("") && !department.getSelectedItem().equals("")&& !depart.getSelectedItem().equals("")&& !semester.getSelectedItem().equals(""))
         {
             theQuery("INSERT INTO course(id,name,hour,min,language,department,semester) "
                     + "VALUES ('"+id.getText()+"','"+course.getText()+"','"+hour.getText()+"','"+sec.getText()+"','"+department.getSelectedItem()+"','"+depart.getSelectedItem()+"','"+semester.getSelectedItem()+"')");
             message.setText("Student Added Succesfully.!!");
             //dispose();
             //new Student.home().setVisible(true);
            id.setText(null);
            course.setText(null);
            hour.setText(null);
            sec.setText(null);
            department.setSelectedItem(null);
            depart.setSelectedItem(null);
            semester.setSelectedItem(null);
         }
         else{
            message.setText("You Need to fill & select all fields.");
        }
         
    }

